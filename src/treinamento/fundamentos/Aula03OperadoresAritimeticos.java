package treinamento.fundamentos;
public class Aula03OperadoresAritimeticos {
    public static void main(String[] args) {
        int valor1 = 10;
        int valor2 = 20;

        System.out.println(valor1 + valor2);
        System.out.println(valor1 * valor2);
        System.out.println(valor1 / valor2);
        System.out.println(valor1 - valor2);
        System.out.println(valor1 % valor2);

    }
}
